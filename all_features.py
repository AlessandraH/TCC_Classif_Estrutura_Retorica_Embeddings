def abstract2labels(abstract):
    return [label for label, abstract in abstract]


def sent2features(i, azport, we, tfidf, tfidf_prev, tfidf_next, pos):
    features = {
        'length': azport[i][0],
        'location': azport[i][1],
        'citacion': azport[i][2],
        'formulaic': azport[i][3],
        'tense': azport[i][4],
        'voice': azport[i][5],
        'modal': azport[i][6],
        'history': azport[i][7],
        'we': f.np.sum(we[i]),
        'tfidf': f.np.sum(tfidf[i]),
        'tfidf_prev': f.np.sum(tfidf_prev[i]),
        'tfidf_next': f.np.sum(tfidf_next[i]),
        'pos': pos[i],
    }

    if pos[i] == 0:
        features['boa'] = True
    elif pos[i + 1] == 0:
        features['eoa'] = True

    return features


def abstract2features(abstract, azport, we, tfidf, tfidf_prev, tfidf_next, pos, c):
    return [sent2features(c+i, azport, we, tfidf, tfidf_prev, tfidf_next, pos) for i in range(len(abstract))]


